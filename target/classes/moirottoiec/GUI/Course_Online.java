    private void addButtonaddLecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonaddLecActionPerformed
        // TODO add your handling code here:
        String URL = lastNameTextField.getText();

        if(URL.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập URL!");
        }else{
            onlineCourse.setURL(URL);
            onlineCourseBLL.addOnlineCourse(onlineCourse);
            JOptionPane.showMessageDialog(this, "Thêm khoa hoc thành công!");
            loadOnlineCourseList();
            lastNameTextField.setText("");
        }
    }//GEN-LAST:event_addButtonaddLecActionPerformed

    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        // TODO add your handling code here:
        onlineCourseBLL = new OnlineCourseBLL();
        int row = studentTable.getSelectedRow();
        onlineCourse = onlineCourseBLL.getOfflineCourseByID((int) studentTable.getValueAt(row, 0));

        CourseDTextField.setText(String.valueOf(onlineCourse.getCourseID()));
        lastNameTextField.setText(onlineCourse.getURL());
        addButton.setEnabled(false);
    }//GEN-LAST:event_studentTableMouseClicked

    private void deleteButtonaddLecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonaddLecActionPerformed
        // TODO add your handling code here:
        OnlineCourseBLL onlBLL = new OnlineCourseBLL();
        int row = studentTable.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(Course_Online.this, "Vui long chon khoa hoc!", "Loi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(Course_Online.this, "Ban chac chan muon xoa chu?");
            if(confirm == JOptionPane.YES_OPTION){
                int courseID = (int) studentTable.getValueAt(row, 0);
                onlBLL.deleteOnlineCourse(courseID);
                CourseDTextField.setText("");
                lastNameTextField.setText("");
                loadOnlineCourseList();
            }
        }
    }//GEN-LAST:event_deleteButtonaddLecActionPerformed

    private void updateButtonaddLecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonaddLecActionPerformed
        // TODO add your handling code here:
        OnlineCourseBLL onlBLL = new OnlineCourseBLL();
        OnlineCourse onlineCourse = new OnlineCourse();
        int row = studentTable.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(Course_Online.this, "Vui long chon khoa hoc can sua!",  "Loi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int courseID = (int) studentTable.getValueAt(row, 0);

            onlineCourse.setCourseID(courseID);
            onlineCourse.setURL(lastNameTextField.getText());
            onlBLL.updateOnlineCourse(onlineCourse);
            CourseDTextField.setText("");
            lastNameTextField.setText("");
        }
        loadOnlineCourseList();
    }//GEN-LAST:event_updateButtonaddLecActionPerformed

