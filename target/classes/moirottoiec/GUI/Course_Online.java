    public void loadOnlineCourseList(){
        dtm = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        dtm.addColumn("CourseID");
        dtm.addColumn("URL");
        studentTable.setModel(dtm);
        OnlineCourseBLL onl = new OnlineCourseBLL();
        Vector<OnlineCourse> arr = new Vector<OnlineCourse>();
        arr = onl.getAllOnlineCourses();
        System.out.println(arr);
        for(int i = 0; i < arr.size(); i++){
            OnlineCourse em = arr.get(i);
            int CourseID = em.getCourseID();
            String URL = em.getURL();
            Object[] row = {CourseID,URL};
            dtm.addRow(row);
        } 
        addButton.setEnabled(true);
    }
    
     public void search(){
        dtm.setRowCount(0);
        OnlineCourseBLL onlineCourseBLL = new OnlineCourseBLL();
        Vector<OnlineCourse> arr = new Vector<OnlineCourse>();
        arr = onlineCourseBLL.findOnlineCourses(tuKhoa);
        for(int i = 0; i < arr.size(); i++){
            OnlineCourse em = arr.get(i);
            int CourseID = em.getCourseID();
            String URL = em.getURL();
            Object[] row = {CourseID, URL};
            dtm.addRow(row);
        }
    
    }

