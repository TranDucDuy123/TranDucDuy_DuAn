    private void addButtonaddLecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonaddLecActionPerformed
        // TODO add your handling code here:
        String lName = lastNameTextField.getText();
        String fName = firstNameTextField.getText();
        String hDate = enrollmentDateTextField.getText();

        if(lName.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ho!");
        }
        else if(fName.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên!");
        }
        else if(hDate.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày thuê!");
        }else{
            student.setLastName(lName);
            student.setFirstName(fName);
            student.setEnrollmentDate(Date.valueOf(enrollmentDateTextField.getText()));
            studentBLL.addStudent(student);
            JOptionPane.showMessageDialog(this, "Thêm giảng viên thành công!");
            loadStudentList();
            lastNameTextField.setText("");
            firstNameTextField.setText("");
            enrollmentDateTextField.setText("");
        }
    }//GEN-LAST:event_addButtonaddLecActionPerformed

    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        // TODO add your handling code here:
        studentBLL = new StudentBLL();
        int row = studentTable.getSelectedRow();
        student = studentBLL.getStudentByID((int) studentTable.getValueAt(row, 0));

        personIDTextField.setText(String.valueOf(student.getPersonID()));
        lastNameTextField.setText(student.getLastName());
        firstNameTextField.setText(student.getFirstName());
        enrollmentDateTextField.setText(String.valueOf(student.getEnrollmentDate()));
        addButton.setEnabled(false);
    }//GEN-LAST:event_studentTableMouseClicked

    private void deleteButtonaddLecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonaddLecActionPerformed
        // TODO add your handling code here:
        StudentBLL stuBLL = new StudentBLL();
        int row = studentTable.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(StudentManagementGUI.this, "Vui long chon sinh vien can xoa!", "Loi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(StudentManagementGUI.this, "Ban chac chan muon xoa chu?");
            if(confirm == JOptionPane.YES_OPTION){
                int personID = (int) studentTable.getValueAt(row, 0);
                stuBLL.deleteStudent(personID);
                 personIDTextField.setText("");
                 lastNameTextField.setText("");
                 firstNameTextField.setText("");
                 enrollmentDateTextField.setText("");
                loadStudentList();
            }
        }
    }//GEN-LAST:event_deleteButtonaddLecActionPerformed

    private void updateButtonaddLecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonaddLecActionPerformed
        // TODO add your handling code here:
        StudentBLL stuBLL = new StudentBLL();
        Student student = new Student();
        int row = studentTable.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(StudentManagementGUI.this, "Vui long chon sinh vien can sua!",  "Loi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int personID = (int) studentTable.getValueAt(row, 0);

            student.setPersonID(personID);
            student.setLastName(lastNameTextField.getText());
            student.setFirstName(firstNameTextField.getText());
            student.setEnrollmentDate(Date.valueOf(enrollmentDateTextField.getText()));
            studentBLL.updateStudent(student);
            personIDTextField.setText("");
            lastNameTextField.setText("");
            firstNameTextField.setText("");
            enrollmentDateTextField.setText("");
            loadStudentList();
        }

    }//GEN-LAST:event_updateButtonaddLecActionPerformed

