    public void search(){
        dtm.setRowCount(0);
        StudentBLL studentBLL = new StudentBLL();
        Vector<Student> arr = new Vector<Student>();
        arr = studentBLL.findStudent(tuKhoa);
        for(int i = 0; i < arr.size(); i++){
            Student em = arr.get(i);
            int PersonID = em.getPersonID();
            String LastName = em.getLastName();
            String FirstName = em.getFirstName();
            Date EnrollmentDate = em.getEnrollmentDate();
            Object[] row = {PersonID, LastName, FirstName , EnrollmentDate};
            dtm.addRow(row);
        }
    
    }
    
